    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed

        if (restriccion.limiteclave(txtclave) && restriccion.limitenrodcto(txtnrodcto) && restriccion.limitetelefono(txttelefono)) {
            cliente.modificarDatosClientes(txtnrodcto, txtnombre, txtclave, txtdireccion, txttelefono);
            try {
                cliente.mostrarTablaClientes(jTable1);
            } catch (SQLException ex) {
                Logger.getLogger(modificarinfo.class.getName()).log(Level.SEVERE, null, ex);
            }

            txtnrodcto.setEnabled(true);

            txtnrodcto.setText("");
            txtnombre.setText("");
            txtclave.setText("");
            txttipodcto.setText("");
            txtdireccion.setText("");
            txttelefono.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "Los datos ingresados no son correctos");
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        admin.seleccionardatosGenerales(jTable1, txtnrodcto, txtnombre, txtclave, txttipodcto, txtdireccion, txttelefono);

        if (cliente.regresarporID(txtnrodcto, txtnombre, txtclave, txtdireccion, txttelefono, txttipodcto)) {
            JOptionPane.showMessageDialog(null, "registro encontrado");

            btnmodificar.setEnabled(true);
            btneliminar.setEnabled(true);
            txtnrodcto.setEnabled(false);

        } else {
            JOptionPane.showMessageDialog(null, "registro NO encontrado");
        }
    }//GEN-LAST:event_jTable1MouseClicked

