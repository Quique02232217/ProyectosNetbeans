    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        cliente.eliminarDatosClientes(txtnrodcto);
        cuenta.eliminarDatosCuentas(txtnrodcto);
        txtnrodcto.setEnabled(true);
        try {

            txtnrodcto.setText("");
            txtnombre.setText("");
            txtclave.setText("");
            txttipodcto.setText("");
            txtdireccion.setText("");
            txttelefono.setText("");

            cliente.mostrarTablaClientes(jTable1);
        } catch (SQLException ex) {
            Logger.getLogger(modificarinfo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btneliminarActionPerformed


